// Bilinear interpolation of geoid separation at given lat/lon
// Returns 0.0 if outside coverage area
inline float egm96GetGeoidSep(double lat, double lon) {
    if (lat > EGM96_LAT_MAX || lat < EGM96_LAT_MIN ||
        lon < EGM96_LON_MIN || lon > EGM96_LON_MAX) {
        return 0.0f;  // outside coverage — caller should flag this
    }

    // Grid indices (row 0 = lat 72°N, counting down)
    float row_f = (EGM96_LAT_MAX - lat) / EGM96_STEP;
    float col_f = (lon - EGM96_LON_MIN)  / EGM96_STEP;

    int r0 = (int)row_f;
    int c0 = (int)col_f;
    int r1 = min(r0 + 1, EGM96_NLAT - 1);
    int c1 = min(c0 + 1, EGM96_NLON - 1);

    float dr = row_f - r0;
    float dc = col_f - c0;

    // Read four surrounding grid points from flash
    float v00 = (float)pgm_read_byte(&EGM96_EUROPE[r0][c0]);
    float v01 = (float)pgm_read_byte(&EGM96_EUROPE[r0][c1]);
    float v10 = (float)pgm_read_byte(&EGM96_EUROPE[r1][c0]);
    float v11 = (float)pgm_read_byte(&EGM96_EUROPE[r1][c1]);

    // Bilinear interpolation
    return v00 * (1-dr) * (1-dc)
         + v01 * (1-dr) *    dc
         + v10 *    dr  * (1-dc)
         + v11 *    dr  *    dc;
}
